def get_crop_image_url(crop_name):
    # Normalize input
    key = str(crop_name).lower().strip()
    
    # 1. Check for exact match in dictionary
    if key in CROP_IMAGES:
        return CROP_IMAGES[key]
    
    # 2. Fuzzy match (if the model outputs 'Rice (Paddy)', it finds 'rice')
    for k, path in CROP_IMAGES.items():
        if k in key:
            return path
            
    # 3. Fallback if no image found (Return None or a placeholder path)
    return None

