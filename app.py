def sanitize_ai_data(data):
    """
    Forces AI data to stay within realistic agricultural limits.
    If a value is crazy (e.g., Temp 100¬∞C), it clamps it to the max limit.
    """
    if not data: return None
    
    cleaned = {}
    # Copy description as is
    cleaned['description'] = data.get('description', 'Weather data estimated.')
    
    # Clean numerical values
    for key, (min_v, max_v, default_v) in SAFE_RANGES.items():
        try:
            val = float(data.get(key, default_v))
            # Clamp value between min and max
            if val < min_v: val = min_v
            if val > max_v: val = max_v
            cleaned[key] = round(val, 1)
        except:
            cleaned[key] = default_v
            
    return cleaned

